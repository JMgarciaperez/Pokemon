ALTER TABLE COMBATE
	ADD CONSTRAINT ID_COMBATE_PK PRIMARY KEY (ID_COMBATE);
  

CREATE TABLE MOVIMIENTO(
    ID_MOVIMIENTO INT(5)NOT NULL,
    NOMBRE_MOV VARCHAR(15) NOT NULL,
    NIVEL INT DEFAULT 1,
    TIPO VARCHAR(7) NOT NULL,
    ESTAMINA INT NOT NULL,
    POTENCIA INT,
    TIPO_ELEMENTO VARCHAR(15) NOT NULL,
    NUM_TURNOS INT(1) DEFAULT 1,
    ESTADO VARCHAR(15) ,
    MEJORA VARCHAR(15),
    CANT_MEJORA INT(2)
    );
    
ALTER TABLE MOVIMIENTO
    ADD CONSTRAINT ID_MOV_PK PRIMARY KEY (ID_MOVIMIENTO),
    ADD CONSTRAINT TIPO_ELEMENTO_CK CHECK (TIPO_ELEMENTO IN('AGUA','FUEGO','PLANTA','VOLADOR','ELECTRICO','NORMAL')),
    ADD CONSTRAINT ESTADO_CK CHECK (ESTADO IN('PARALIZADO','DORMIDO','QUEMADO','SIN_ESTADO'));

CREATE TABLE MOVIMIENTO_POKEMON(
    ID_MOVIMIENTO INT(5) NOT NULL,
    ID_POKEMON INT(5)NOT NULL,
    LISTA_MOV INT
    );
    
ALTER TABLE MOVIMIENTO_POKEMON
    ADD CONSTRAINT ID_MOV_POK_PK PRIMARY KEY (ID_MOVIMIENTO, ID_POKEMON),
    ADD CONSTRAINT LISTA_MOV_CK CHECK (LISTA_MOV IN(1,2));
    

CREATE TABLE TURNO(
    ID_COMBATE INT(4) NOT NULL,
	NUM_TURNO INT(2) NOT NULL,
    ATAQUE_ENTRENADOR VARCHAR(40),
    ATAQUE_RIVAL VARCHAR(40)
    );
    
ALTER TABLE TURNO 
    ADD CONSTRAINT NUM_TURNO_PK PRIMARY KEY (NUM_TURNO,ID_COMBATE),
	ADD CONSTRAINT ID_COMB_FK FOREIGN KEY (ID_COMBATE) REFERENCES COMBATE(ID_COMBATE);

ALTER TABLE COMBATE 
	ADD CONSTRAINT ID_ENTRENADOR_FK FOREIGN KEY (JUGADOR_ENTRENADOR) REFERENCES ENTRENADOR(ID_ENTRENADOR);
	
ALTER TABLE POKEMON_ENTRENADOR
	ADD CONSTRAINT ID_ENTRENA_FK FOREIGN KEY (ID_ENTRENADOR) REFERENCES ENTRENADOR(ID_ENTRENADOR),
	ADD CONSTRAINT NUM_POKEDEX_FK FOREIGN KEY (NUM_POKEDEX) REFERENCES POKEDEX(NUM_POKEDEX);
	
ALTER TABLE MOVIMIENTO_POKEMON 
